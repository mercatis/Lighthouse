package com.mercatis.lighthouse3.ui.swimlaneeditor.providers;

import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.jface.viewers.ILabelProvider;
import org.eclipse.jface.viewers.ILabelProviderListener;
import org.eclipse.swt.graphics.Image;

import com.mercatis.lighthouse3.base.ui.provider.LabelConverter;
import com.mercatis.lighthouse3.domainmodel.processinstance.ProcessInstance;


public class ProcessInstanceLabelProvider implements ILabelProvider {

	private static ImageDescriptor IMAGE_ERROR = ImageDescriptor.createFromURL(ProcessInstanceLabelProvider.class.getResource("/icons/red_x16.png"));
	private static ImageDescriptor IMAGE_OK = ImageDescriptor.createFromURL(ProcessInstanceLabelProvider.class.getResource("/icons/green_x16.png"));
	private static ImageDescriptor IMAGE_OPEN = ImageDescriptor.createFromURL(ProcessInstanceLabelProvider.class.getResource("/icons/white_x16.png"));
	
	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ILabelProvider#getImage(java.lang.Object)
	 */
	public Image getImage(Object element) {
		ProcessInstance instance = (ProcessInstance) element;
		if (!instance.isClosed()) {
			return IMAGE_OPEN.createImage();
		} else if (instance.isErroneous()) {
			return IMAGE_ERROR.createImage();
		} else {
			return IMAGE_OK.createImage();
		}
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ILabelProvider#getText(java.lang.Object)
	 */
	public String getText(Object element) {
		return LabelConverter.getLabel(element);
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IBaseLabelProvider#addListener(org.eclipse.jface.viewers.ILabelProviderListener)
	 */
	public void addListener(ILabelProviderListener listener) {
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IBaseLabelProvider#dispose()
	 */
	public void dispose() {
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IBaseLabelProvider#isLabelProperty(java.lang.Object, java.lang.String)
	 */
	public boolean isLabelProperty(Object element, String property) {
		return false;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.IBaseLabelProvider#removeListener(org.eclipse.jface.viewers.ILabelProviderListener)
	 */
	public void removeListener(ILabelProviderListener listener) {
	}

}
